
SELECT B.BIENROTRAB, b.BIEFHTRAEM ,B.BIECNTAFEC ,B.BIEESTADO ,B.BIECNTPEMB, B.BIELOTEPLA  ,B.BIECNTFINU, B.BIECNTINIS, B.BiECntIniC ,S.STKIDSTOCK ,S.STKCNTSTK ,L.LOTCANTIDA ,L.LOTUSULTUP 
