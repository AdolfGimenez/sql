INNER JOIN GXTRJDTA.TSTLOT L ON L.STKIDSTOCK =  S.STKIDSTOCK 
WHERE b.BIENROTRAB  = '20200507_1020_1' AND L.LOTNROLOTE  = 1 AND BIEFHTRAEM ='2020-05-07 09:20:49';

